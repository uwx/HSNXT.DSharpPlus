namespace System.Collections.Generic
{
    /// <summary>
    /// Exposes a ForEach&lt;T&gt; extension method on IEnumerable&lt;T&gt;, just like List&lt;T&gt; does.
    /// </summary>
    ///	<nuget id="netfx-System.Collections.Generic.IEnumerable.ForEach" />
    static partial class ForEachExtension
    {
        /// <summary>
        /// Iterates all source elements and executes the given action for each.
        /// </summary>
        /// <param name="source" this="true">The source of items to iterate.</param>
        /// <param name="action">The action to execute for each item in the source.</param>
        public static void ForEach<T>(this IEnumerable<T> source, Action<T> action)
        {
            foreach (var item in source)
            {
                action(item);
            }
        }
    }
}