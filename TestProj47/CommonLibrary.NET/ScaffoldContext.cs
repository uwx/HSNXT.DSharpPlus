using System.Collections.Generic;

namespace HSNXT.ComLib.Scaffolding
{
    public class ScaffoldContext : ActionContext
    {
        /// <summary>
        /// The id of the parent control that will contain the autogenerated
        /// UI controls representing the data for any Business Entity.
        /// </summary>
        public string ParentControlId;


        /// <summary>
        /// The fully-qualified name of the business entity for which to build
        /// a UI for.
        /// </summary>
        public string EntityName;


        /// <summary>
        /// The id of the entity to update/delete.
        /// </summary>
        public string EntityId;


        /// <summary>
        /// List of key/value pairs corresponding the properties on the Entity.
        /// </summary>
        public IList<KeyValuePair<string, string>> PropValues;


        /// <summary>
        /// Initialize the scaffold context.
        /// </summary>
        /// <param name="errors"></param>
        /// <param name="messages"></param>
        /// <param name="entityName"></param>
        /// <param name="parentControlId"></param>
        public ScaffoldContext(IValidationResults errors, IErrors messages, string entityName, string entityId, string parentControlId)
            : base(errors)
        {
            EntityName = entityName;
            ParentControlId = parentControlId;
            EntityId = entityId;
        }
    }
}
